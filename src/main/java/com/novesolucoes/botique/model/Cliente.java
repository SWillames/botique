package com.novesolucoes.botique.model;

import java.io.Serializable;
import java.util.Objects;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.SequenceGenerator;

/**
 *
 * @author sergio
 */
@Entity
public class Cliente implements Serializable {
    
    @SequenceGenerator(name="clienteGenerator",
                       sequenceName="CLIENTE_SEQ",
                       allocationSize=0)
    @Id
    @GeneratedValue(strategy = GenerationType.SEQUENCE,
                    generator="clienteGenerator")
    private Long id;
    private String nome;
    private String cpf;
    private String endereco;
    private Integer faixasalarial;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public Integer getFaixasalarial() {
        return faixasalarial;
    }

    public void setFaixasalarial(Integer faixasalarial) {
        this.faixasalarial = faixasalarial;
    }

    @Override
    public int hashCode() {
        int hash = 3;
        hash = 29 * hash + Objects.hashCode(this.id);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Cliente other = (Cliente) obj;
        if (!Objects.equals(this.id, other.id)) {
            return false;
        }
        return true;
    }
    }
